#include <stdio.h>
#include <stdlib.h>
int main() {
    int x = 100;
    printf("Value of x: %d\n", x);
    float y = 3.14f;
    printf("Value of y: %.2f\n", y);
    char z = 'A';
    printf("Value of z: %c\n", z);
    printf("Hello, World!\n");
    printf("Goodbye, World!\n");
    printf("This is a C++17 program.\n");
    printf("End of program.\n");
    double pi = 3.14159;
    printf("Value of pi: %.5f\n", pi);
    return 0;
}